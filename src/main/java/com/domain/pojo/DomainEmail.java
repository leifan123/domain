package com.domain.pojo;

import java.util.Date;

import java.io.Serializable;

public class DomainEmail  implements Serializable{

	private static final long serialVersionUID = 153421177926183418L;
	private Integer id;

	public DomainEmail setId(Integer id){
		 this.id = id;
		 return this;
	}

	public Integer getId(){
		 return id;
	}
	private String email;

	public DomainEmail setEmail(String email){
		 this.email = email;
		 return this;
	}

	public String getEmail(){
		 return email;
	}
	private Integer status;

	public DomainEmail setStatus(Integer status){
		 this.status = status;
		 return this;
	}

	public Integer getStatus(){
		 return status;
	}
	private Date createtime;

	public DomainEmail setCreatetime(Date createtime){
		 this.createtime = createtime;
		 return this;
	}

	public Date getCreatetime(){
		 return createtime;
	}
	private Date completetime;

	public DomainEmail setCompletetime(Date completetime){
		 this.completetime = completetime;
		 return this;
	}

	public Date getCompletetime(){
		 return completetime;
	}
	private String remark;

	public DomainEmail setRemark(String remark){
		 this.remark = remark;
		 return this;
	}

	public String getRemark(){
		 return remark;
	}
}